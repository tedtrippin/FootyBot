package com.rob.betBot.mvc.model;

public class EventParent {

    private String name;
    private String url;

    public EventParent() {
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getUrl() {
        return url;
    }

    public void setUrl(String url) {
        this.url = url;
    }
}
